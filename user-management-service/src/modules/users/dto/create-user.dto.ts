import { IsEmail, IsNotEmpty, IsOptional, IsString, MinLength } from 'class-validator';

export class CreateUserDto {
  @IsEmail()
  email: string;

  @IsString()
  @MinLength(8)
  password: string;

  @IsOptional()
  @IsNotEmpty()
  locale?: string;

  @IsOptional()
  @IsNotEmpty()
  role?: string;
}
